// 합계 알고리즘(Sum Algorithm): 주어진 범위에 주어진 조건에 해당하는 값(Value)들의 합계.

const a = 1;
const b = 2;
const c = 3;

console.log(a + b);
console.log(a + c);
console.log(b + c);

var x;

x = 10;
console.log(x); //10

x += 5;
console.log(x); //15

x -= 5;
console.log(x); //10

x *= 5;
console.log(x); //50

x /= 5;
console.log(x); //10

x %= 5;
console.log(x); //0
